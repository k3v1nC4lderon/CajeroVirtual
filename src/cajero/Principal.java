    private void bCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCrearActionPerformed
        vNombre = tNombre.getText();
        vAp = tAp.getText();
        vAm = tAm.getText();
        vCi = Integer.parseInt(tCi.getText());
        vDia = Integer.parseInt(tDia.getText());
        vMes = Integer.parseInt(tMes.getText());
        vAnio = Integer.parseInt(tAnio.getText());
        if (bolivianos.isSelected()) {
            tipoCuenta = "Bolivianos";
        } else if (dolares.isSelected()) {
            tipoCuenta = "Dolares";
        } else if (euros .isSelected()) {
            tipoCuenta = "Euros";
        }else{
            tipoCuenta = "Bolivianos";
        }
        Cuentas cuenta = new Cuentas(vNombre, vAp, vAm, vCi, vDia,vMes,vAnio,tipoCuenta);
        if(cuenta.leerCuentasCrear()){
            errorRegistro.setText(idioma ? "YA EXISTE UNA CUENTA ASI" : "Account Already Exists");

            
        }else{
            Validar v = new Validar(vNombre, vAp, vAm, vCi, vDia,vMes,vAnio,tipoCuenta, idioma);
            if(!v.crearCuentaDistinta(vCi, tipoCuenta)){
            v.setVisible(true);
            this.dispose();
            }else{
                 errorRegistro.setText(idioma ? "Se creo una cuenta con el mismo Pin y Usuario del CI ingresado." : "A account was created with the same PIN and User as the entered ID.");
            }
            
        }
        
// TODO add your handling code here:
    }//GEN-LAST:event_bCrearActionPerformed

